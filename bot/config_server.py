"""
TradeOracle AI — Secure Config Server
======================================
Runs alongside the bot.
Admin portal talks to this to save/load settings.
All sensitive data encrypted at rest.
"""
from http.server import HTTPServer, BaseHTTPRequestHandler
import json, os, hashlib, hmac, base64, secrets
from datetime import datetime

CONFIG_FILE = "/opt/tradeoracleai/config/settings.json"
SECRET_KEY_FILE = "/opt/tradeoracleai/config/secret.key"
CONFIG_DIR = "/opt/tradeoracleai/config"

os.makedirs(CONFIG_DIR, exist_ok=True)

# ── Generate or load secret key ──
def get_secret_key():
    if not os.path.exists(SECRET_KEY_FILE):
        key = secrets.token_hex(32)
        with open(SECRET_KEY_FILE, 'w') as f:
            f.write(key)
        os.chmod(SECRET_KEY_FILE, 0o600)
        return key
    return open(SECRET_KEY_FILE).read().strip()

SECRET_KEY = get_secret_key()

# ── Simple XOR encryption for secrets ──
def encrypt(text, key):
    key_bytes = key.encode()
    text_bytes = text.encode()
    encrypted = bytes([text_bytes[i] ^ key_bytes[i % len(key_bytes)]
                       for i in range(len(text_bytes))])
    return base64.b64encode(encrypted).decode()

def decrypt(encrypted_text, key):
    key_bytes = key.encode()
    encrypted_bytes = base64.b64decode(encrypted_text.encode())
    decrypted = bytes([encrypted_bytes[i] ^ key_bytes[i % len(key_bytes)]
                       for i in range(len(encrypted_bytes))])
    return decrypted.decode()

# ── Load config ──
def load_config():
    if not os.path.exists(CONFIG_FILE):
        return {
            "telegram_token": "",
            "admin_chat_id": "",
            "anthropic_key": "",
            "gmail_sender": "",
            "gmail_password": "",
            "scan_interval": "15",
            "briefing_time": "08:00",
            "watchlist": "NVDA,AAPL,MSFT,GOOGL,NFLX,AMZN,META,TSLA",
            "min_profit_alert": "7",
            "warn_buffer": "12",
            "danger_buffer": "5",
            "bot_username": "@TradeOracleAIBot",
            "admin_email": "rishisharma1510@gmail.com",
            "configured": False,
            "last_updated": None
        }
    with open(CONFIG_FILE) as f:
        data = json.load(f)
    # Decrypt sensitive fields
    for field in ['telegram_token', 'anthropic_key', 'gmail_password']:
        if data.get(field) and data[field].startswith('ENC:'):
            try:
                data[field] = decrypt(data[field][4:], SECRET_KEY)
            except:
                data[field] = ""
    return data

# ── Save config ──
def save_config(data):
    to_save = data.copy()
    # Encrypt sensitive fields
    for field in ['telegram_token', 'anthropic_key', 'gmail_password']:
        if to_save.get(field):
            to_save[field] = 'ENC:' + encrypt(to_save[field], SECRET_KEY)
    to_save['last_updated'] = datetime.now().isoformat()
    to_save['configured'] = bool(
        data.get('telegram_token') and
        data.get('anthropic_key') and
        data.get('admin_chat_id')
    )
    with open(CONFIG_FILE, 'w') as f:
        json.dump(to_save, f, indent=2)
    os.chmod(CONFIG_FILE, 0o600)
    # Also write .env for the bot process
    write_env(data)
    return to_save

# ── Write .env file for bot ──
